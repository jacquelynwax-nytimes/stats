// reducer
export default function(state = initialState, action) {
  switch (action.type) {
    case SET_USER:
      return Object.assign({}, state, {user: action.user})
    case REMOVE_USER:
      return Object.assign({}, state, {user: {}})
    case SET_USER_WEEK_STATS:
      return Object.assign({}, state, {userStatsThisWeek: action.stats})
    case REMOVE_USER_WEEK_STATS:
      return Object.assign({}, state, {userStatsThisWeek: {}})
    default:
      return state
  }
}
